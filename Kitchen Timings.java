class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
        while (t-- > 0) {
            int x = sc.nextInt();
            int y = sc.nextInt();
            int hours = (y - x + 12) % 12; // calculate the number of hours Chef works
            System.out.println(hours);
        }
        sc.close();
		// your code goes here
	}
}
